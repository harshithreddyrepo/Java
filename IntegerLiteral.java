// representing integer literal in binary,octal and hexadecimal number systems
public class IntegerLiteral {
    public static void main(String[] args) {
        int a1=10;  // decimal number system
        int a2=0b1010;   // binary number system
        int a3=012;   // octal number system
        int a4=0xA;  // hexadecimal number system

        System.out.println("a1:"+a1);
        System.out.println("a2:"+a2);
        System.out.println("a3:"+a3);
        System.out.println("a4:"+a4);

    }
}
